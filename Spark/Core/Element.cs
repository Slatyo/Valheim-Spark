namespace Spark.Core
{
    /// <summary>
    /// Elemental types for VFX effects.
    /// </summary>
    public enum Element
    {
        /// <summary>No element (neutral).</summary>
        None = 0,

        /// <summary>Fire - flames, embers, heat distortion.</summary>
        Fire,

        /// <summary>Frost - ice crystals, cold mist, snowflakes.</summary>
        Frost,

        /// <summary>Lightning - sparks, bolts, electricity arcs.</summary>
        Lightning,

        /// <summary>Poison - toxic clouds, drips, bubbles.</summary>
        Poison,

        /// <summary>Spirit - ethereal wisps, holy glow.</summary>
        Spirit,

        /// <summary>Shadow - dark particles, void effects.</summary>
        Shadow,

        /// <summary>Arcane - magic sparkles, runes.</summary>
        Arcane
    }
}
